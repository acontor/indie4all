            'nombre' => $request->get('nombre'),
            'descripcion' => $request->get('descripcion'),
            'icono' => $request->get('icono'),
        ]);

        $logro->save();

        return redirect('/admin/logros')->with('success', 'Â¡Logro guardado!');
    }

